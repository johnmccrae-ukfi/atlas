# 
# ### Architectural Responsibilities
# 
# This notebook is responsible for:
# 
# - Reading deterministic AI-ready market features
# - Validating the upstream AI feature contract
# - Preparing AI inference requests
# - Calling a configurable AI provider through an abstraction layer
# - Generating concise natural-language market commentary
# - Recording inference metadata for traceability and auditing
# - Persisting AI commentary as a Delta table
# - Validating the generated output before completion
# 
# This notebook is **not responsible** for:
# 
# - Market data ingestion
# - Feature engineering
# - Technical indicator calculation
# - Trading signals
# - Forecasting
# - Buy or sell recommendations
# - Modifying deterministic analytical outputs
