# ------------------------------------------------------------
# Helper: Build Stub Commentary
# ------------------------------------------------------------

def generate_stub_commentary(row) -> str:
    """
    Generate deterministic placeholder commentary from analytical features.

    This is used to validate the AI inference architecture before integrating
    with an external LLM provider such as Fabric AI or Azure OpenAI.
    """
    return (
        f"{row['Instrument']} traded {row['PriceDirection'].lower()} on "
        f"{row['TradingDate']}. The session was classified as a "
        f"{row['SessionCharacter']}, with {row['VolatilityBand'].lower()} "
        f"volatility and {row['ActivityBand'].lower()} trading activity."
    )


# ------------------------------------------------------------
# Helper: Generate Market Commentary
# ------------------------------------------------------------

def generate_market_commentary(row) -> dict:
    """
    Generate market commentary using the configured AI provider.

    The notebook calls this function without needing to know which provider
    generated the commentary. This keeps AI inference isolated from the
    orchestration logic.
    """
    try:
        if AI_PROVIDER == AIProvider.STUB:
            commentary = generate_stub_commentary(row)

            return {
                "Commentary": commentary,
                "InferenceStatus": "Succeeded",
                "InferenceError": None,
                "ModelProvider": AI_PROVIDER,
                "ModelName": MODEL_NAME
            }

        elif AI_PROVIDER == AIProvider.FABRIC:
            raise NotImplementedError(
                "Fabric AI provider has not yet been implemented."
            )

        elif AI_PROVIDER == AIProvider.AZURE_OPENAI:
            raise NotImplementedError(
                "Azure OpenAI provider has not yet been implemented."
            )

        elif AI_PROVIDER == AIProvider.PHI:
            raise NotImplementedError(
                "Phi provider has not yet been implemented."
            )

        else:
            raise ValueError(f"Unsupported AI provider: {AI_PROVIDER}")

    except Exception as error:
        return {
            "Commentary": None,
            "InferenceStatus": "Failed",
            "InferenceError": str(error),
            "ModelProvider": AI_PROVIDER,
            "ModelName": MODEL_NAME
        }


