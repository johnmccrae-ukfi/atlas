def table_exists(table_name: str) -> bool:
    """
    Check whether a table exists in the current Lakehouse context.
    """
    try:
        spark.table(table_name)
        return True
    except Exception:
        return False


# ------------------------------------------------------------
# Helper: Display DataFrame Summary
# ------------------------------------------------------------

def display_dataframe_summary(df: DataFrame, dataframe_name: str) -> None:
    """
    Display basic record count and schema information for a DataFrame.
    """
    row_count = df.count()

    print(f"DataFrame: {dataframe_name}")
    print(f"Row Count : {row_count}")
    print("Schema:")
    df.printSchema()


# ------------------------------------------------------------
# Helper: Validate Required Columns
# ------------------------------------------------------------

def validate_required_columns(df: DataFrame, required_columns: list[str], dataframe_name: str) -> None:
    """
    Validate that a DataFrame contains all required columns.
    """
    missing_columns = [column for column in required_columns if column not in df.columns]

    if missing_columns:
        raise ValueError(
            f"{dataframe_name} is missing required columns: {missing_columns}"
        )

    print(f"{dataframe_name} contains all required columns.")


# ------------------------------------------------------------
# Helper: Build Input Hash
# ------------------------------------------------------------

def add_input_hash(df: DataFrame) -> DataFrame:
    """
    Add a stable hash representing the deterministic input used for commentary generation.
    This allows generated commentary to be traced back to a specific analytical input state.
    """
    return df.withColumn(
        "InputHash",
        sha2(
            concat_ws(
                "||",
                coalesce(col("Instrument").cast("string"), lit("")),
                coalesce(col("TradingDate").cast("string"), lit("")),
                coalesce(col("PromptVersion").cast("string"), lit("")),
                coalesce(col("PromptTemplate").cast("string"), lit(""))
            ),
            256
        )
    )


